package homework;

public class Task3_Main_Exercises {
	public static void main(String[] args) {
		
		int a,b;
		
		a = 10;
		b = 10;
		
		System.out.println("With a = 10. ++a (prefix notation) resulted in: " + ++a  +" That is because value is incremented by 1 and immediately used in our statement");
		System.out.println("With b = 10. b++ (postfix) resulted in: " + b++  +" That is because value is incremented by 1 but the old value gets used in the statement");
	}
}
